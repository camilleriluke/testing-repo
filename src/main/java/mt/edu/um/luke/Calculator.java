package mt.edu.um.luke;

/**
 * The Calulator class.
 *
 * @author <a href="mailto:luke.camilleri@ixaris.com">Luke</a>
 */
public class Calculator {
    public int add(int i, int i1) {
        return i+i1;
    }

    public int minus(int i, int i1) {
        return i - i1;
    }

    public int times(int i, int i1) {
        return i * i1;
    }

    public double sqrt(double i) {
        return Math.sqrt(i);
    }
}
